#include <stdio.h>

/**
  * main - takes no arguments, and prints the name of the program.
  * @argc: int variable counting the number of arguments passed.
  * @argv: a list containing all the arguments passed.
  * Return: returns 0;
  */

int main(int argc, char *argv[])
{
	(void) argc;
	printf("%s\n", argv[0]);
	return (0);
}
